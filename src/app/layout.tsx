import Notice from '@/components/Notice';
import Header from '@/components/PageHeader';
import { ReactQueryProvider } from '@/components/ReactQueryProvider';
import { ThemeProvider } from '@/components/ThemeProvider';
import { config } from '@fortawesome/fontawesome-svg-core';
import '@fortawesome/fontawesome-svg-core/styles.css';
import { dehydrate, HydrationBoundary } from '@tanstack/react-query';
import { Analytics } from '@vercel/analytics/react';
import { SpeedInsights } from '@vercel/speed-insights/next';
import { Metadata, ResolvingMetadata } from 'next';
import { Maven_Pro } from 'next/font/google';
import { PublicationQuery, usePublicationQuery } from '../../generated/graphq';
import getQueryClient from '../utils/getQueryClient';
import { getAutogeneratedPublicationOG } from '../utils/social/og';
import './globals.css';

const ubuntu = Maven_Pro({ subsets: ['latin'] });

const host = process.env.NEXT_PUBLIC_HASHNODE_PUBLICATION_HOST as string;
config.autoAddCss = false;

export async function generateMetadata(
  parent: ResolvingMetadata
): Promise<Metadata> {
  const queryClient = getQueryClient();

  await queryClient.prefetchQuery({
    queryKey: usePublicationQuery.getKey({ host }),
    queryFn: usePublicationQuery.fetcher({ host }),
  });

  const data = queryClient.getQueryData<PublicationQuery>(
    usePublicationQuery.getKey({ host })
  );

  if (!data?.publication) return {};

  return {
    title: data.publication.author.name,
    description:
      data.publication.descriptionSEO ||
      `${data.publication.author.name}'s Web Journal` ||
      '',
    metadataBase: new URL(
      process.env.NEXT_PUBLIC_SITE_URL || 'https://neerajadhav.in'
    ),
    icons: {
      icon: [
        { url: '/Fs-16x16.png', sizes: '16x16', type: 'image/png' },
        { url: '/Fs-32x32.png', sizes: '32x32', type: 'image/png' },
        { url: '/Fs-48x48.png', sizes: '48x48', type: 'image/png' },
      ],
      apple: '/apple-touch-icon.png',
      shortcut: '/favicon.ico',
    },
    alternates: {
      canonical: new URL(
        process.env.NEXT_PUBLIC_SITE_URL || 'https://neerajadhav.in'
      ),
    },
    twitter: {
      card: 'summary_large_image',
      title:
        data.publication.displayTitle ||
        data.publication.title ||
        'Web Journal',
      description:
        data.publication.descriptionSEO ||
        data.publication.title ||
        `${data.publication.author.name}'s Blog` ||
        '',
      images: [
        {
          url:
            data.publication.ogMetaData?.image ||
            getAutogeneratedPublicationOG(data.publication),
          width: 1200,
          height: 630,
        },
      ],
    },
    openGraph: {
      title:
        data.publication.displayTitle ||
        `${data.publication.author.name}'s Blog` ||
        'Neeraj Adhav',
      url: new URL(
        process.env.NEXT_PUBLIC_SITE_URL || 'https://neerajadhav.in'
      ),
      type: 'website',
      description:
        data.publication.descriptionSEO ||
        data.publication.title ||
        `${data.publication.author.name}'s Blog` ||
        'Neeraj Adhav',
      images: [
        {
          url:
            data.publication.ogMetaData?.image ||
            getAutogeneratedPublicationOG(data.publication),
          width: 1200,
          height: 630,
        },
      ],
    },
    manifest: '/manifest.json',
  };
}

export default async function RootLayout({
  children,
}: Readonly<{ children: React.ReactNode }>) {
  const queryClient = getQueryClient();

  await queryClient.prefetchQuery({
    queryKey: usePublicationQuery.getKey({ host }),
    queryFn: usePublicationQuery.fetcher({ host }),
  });

  const data = queryClient.getQueryData<PublicationQuery>(
    usePublicationQuery.getKey({ host })
  );

  if (!data?.publication)
    throw new Error('Please check the host name in your .env file');

  return (
    <html lang='en'>
      <body className={`${ubuntu.className} bg-[#f6f6f6] dark:bg-black`}>
        <ThemeProvider
          attribute='class'
          defaultTheme='system'
          enableSystem
          disableTransitionOnChange
        >
          <ReactQueryProvider>
            <HydrationBoundary state={dehydrate(queryClient)}>
              <Header />
              <Notice />
              {children}
              <SpeedInsights />
              <Analytics />
              {/* <Navbar /> */}
            </HydrationBoundary>
          </ReactQueryProvider>
        </ThemeProvider>
      </body>
    </html>
  );
}
